                        <p class="card-text text-xs-left">The Ryan White HIV/AIDS Program provides a comprehensive system of care that includes primary medical
                          care and essential support services for people living with HIV who are uninsured or underinsured.
                          The Program works with cities, states, and local community-based organizations to provide HIV care
                          and treatment services to more than half a million people each year. The Program reaches approximately
                          52% of all people diagnosed with HIV in the United States. The majority of Ryan White HIV/AIDS
                          Program funds support primary medical care and essential support services. A smaller but equally
                          critical portion is used to fund technical assistance, clinical training, and the development of
                          innovative models of care. The Program serves as an important source of ongoing access to HIV medication
                          that can enable people living with HIV to live close to normal lifespans. August 18, 2015 marked
                          the 25th anniversary of the Ryan White Comprehensive AIDS Resources Emergency (CARE) Act, the legislation
                          that created the Ryan White HIV/AIDS Program. First authorized in 1990, the Program is funded at
                          $2.32 billion in fiscal year 2016. The Program is administered by the U.S. Department of Health
                          and Human Services (HHS), Health Resources and Services Administration (HRSA), HIV/AIDS Bureau
                          (HAB).</p>
